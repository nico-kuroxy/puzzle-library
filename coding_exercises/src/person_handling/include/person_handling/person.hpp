#ifndef _PERSON_HANDLER_PERSON_HPP_
#define _PERSON_HANDLER_PERSON_HPP_

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///> DEPENDENCIES
//> C/C++ libraries
#include <string>
//> Custom-made libraries
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///> STRUCTURE DECLARATION
struct Person {
/**
 * @struct Person
 * @brief Holds the data about each person, ie its name and age.
 *
 */

 public:
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ///> CONSTRUCTORS
    /**
     * @brief Constructs a Person object.
     */
    explicit Person() {
      // Initialize attributes
      this->name_ = "Doe";
      this->age_ = 0;
    }
    /**
     * @brief Constructs a Person object with the specified name and age.
     */
    explicit Person(std::string _name, unsigned int _age) {
      // Initialize attributes
      this->name_ = _name;
      this->age_ = _age;
    }
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ///> DESTRUCTORS
    /**
     * @brief Destructor for the Person struct, cleans up any resources allocated by Person.
     */
    ~Person() {}
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

 public:
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    ///> GENERAL ATTRIBUTES
    //> DYNAMIC / They can change during runtime.
    /* Last name of the person */
    std::string name_; 
    /* Age of the person in years. */
    unsigned int age_;
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
};
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

#endif  // _PERSON_HANDLER_PERSON_HPP_
